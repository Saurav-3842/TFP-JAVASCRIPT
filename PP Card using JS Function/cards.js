const createLabel = (parent, item, indexLabel) => {
  const labelElement = document.createElement("label");
  labelElement.innerHTML = item.name;
  if (item.isActive) {
    labelElement.id = "active";
  }
  parent.appendChild(labelElement);
  const cd = () => {
    fun(item, indexLabel);
  };
  labelElement.addEventListener("click", () => {
    fun(item, indexLabel);
  });
};
const fun = (item, indexLabel) => {
  sideNav.innerHTML = "";
  navData.forEach((label, index) => {
    label.isActive = index === indexLabel;
  });
  // console.log(navData);
  const otherDiv = document.querySelector(".otherTab");
  createNav(navData);
  if (item.name === "Videos") {
    createBox(videoData);
    otherDiv.style.display = "none";
  } else {
    box.innerHTML = "";
    createOtherList();
  }
};
