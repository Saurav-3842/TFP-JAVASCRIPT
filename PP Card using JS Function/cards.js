const box = document.getElementsByClassName("box")[0];
//--------------------------ADD IMAGE FUNCTION----------------------------
const addImage = ({ parent, source, customClass }) => {
  const imageCard = document.createElement("img");
  imageCard.className = customClass;
  imageCard.src = source;
  parent.appendChild(imageCard);
};
//------------------ADD SMALL BOX IN CHILD 1 WITH IMAGE-------------
const addSmallBox = ({ parent, source, time, customClass }) => {
  const smallBox = document.createElement("div");
  smallBox.className = customClass;
  if (time) {
    smallBox.innerText = time;
  }
  if (source) {
    addImage({ parent: smallBox, source: source, customClass: "hicons" });
  }
  parent.appendChild(smallBox);
};
//-------------------------ADD ANCHOR TAG FUNCTION------------------
const addTitle = ({ parent, title, customClass }) => {
  const atag = document.createElement("a");
  atag.className = customClass;
  atag.innerHTML = title;
  parent.appendChild(atag);
};
//-------------------------ADD SPAN TAG FUNCTION-------------------------

const addSpanTag = ({ parent, content, customClass }) => {
  const spanTag = document.createElement("span");
  spanTag.className = customClass;
  if (content) {
    spanTag.innerHTML = content;
  }
  parent.appendChild(spanTag);
};
const addChild1 = (
  parent,
  { defaultImage, hoverImage, smallbox1, smallbox2, duration }
) => {
  const child1 = document.createElement("div");
  child1.className = "child1";
  addImage({ parent: child1, source: hoverImage, customClass: "hover-image" });
  addImage({
    parent: child1,
    source: defaultImage,
    customClass: "default-image",
  });
  addSmallBox({ parent: child1, source: smallbox1, customClass: "smallbox1" });
  addSmallBox({ parent: child1, source: smallbox2, customClass: "smallbox2" });
  addSmallBox({ parent: child1, time: duration, customClass: "timer" });
  parent.appendChild(child1);
};
const addChild2 = (parent, { title, child2Dot }) => {
  const child2 = document.createElement("div");
  child2.className = "child2";
  addTitle({ parent: child2, title: title, customClass: "content-text" });
  addImage({ parent: child2, source: child2Dot, customClass: "icondot" });
  parent.appendChild(child2);
};

const addChild3 = (parent, { views, uploadTime }) => {
  const child3 = document.createElement("div");
  child3.className = "child3";
  addSpanTag({ parent: child3, content: views, customClass: "child3text" });
  addSpanTag({ parent: child3, customClass: "child3dot" });
  addSpanTag({
    parent: child3,
    content: uploadTime,
    customClass: "child3text",
  });

  parent.appendChild(child3);
};

const createCard = (tempDiv, elem, idx) => {
  const mainCard = document.createElement("div");
  mainCard.className = "child";
  mainCard.setAttribute("id", idx);
  addChild1(mainCard, elem);
  addChild2(mainCard, elem);
  addChild3(mainCard, elem);
  tempDiv.appendChild(mainCard);
  const cb = () => {
    funcPage(idx);
  };
  mainCard.addEventListener("click", cb);
};

const funcPage = (id) => {
  const url = `../PP Card using JS Function/VideoPage.html?Search&id=${id}`;
  window.location.href = url;
};

const createBox = (data) => {
  const tempDiv = new DocumentFragment();
  data.forEach((elem, idx) => createCard(tempDiv, elem, idx));
  box.appendChild(tempDiv);
};
